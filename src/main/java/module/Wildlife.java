package module;

public abstract class Wildlife {
    public String name;
    public  int animalId;
    public int id;
    public String health;
    public String age;
    public String type;

    public String getName() {
        return name;
    }

    public int getAnimalId() {
        return animalId;
    }

    public int getId() {
        return id;
    }

    public String getHealth() {
        return health;
    }

    public String getAge() {
        return age;
    }

    public String getType() {
        return type;
    }
}
